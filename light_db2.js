// 2. [수정] 설문 점수를 64괘 결과로 변환하는 핵심 함수
function calculateAndDisplayResult(answers) {
    const scores = {
        건: { outer: 0, inner: 0 }, 태: { outer: 0, inner: 0 },
        리: { outer: 0, inner: 0 }, 진: { outer: 0, inner: 0 },
        손: { outer: 0, inner: 0 }, 감: { outer: 0, inner: 0 },
        간: { outer: 0, inner: 0 }, 곤: { outer: 0, inner: 0 }
    };

    // 답변 점수 배분
    answers.forEach((val, idx) => {
        const q = questions[idx];
        scores[q.trigram][q.type] += (val * q.weight);
    });

    // 최고 점수 괘 찾기
    let upperName = "건", lowerName = "곤", maxO = -999, maxI = -999;
    for (const [name, s] of Object.entries(scores)) {
        if (s.outer > maxO) { maxO = s.outer; upperName = name; }
        if (s.inner > maxI) { maxI = s.inner; lowerName = name; }
    }

    const fullKey = trigramInfo[upperName] + trigramInfo[lowerName];
    const data = ichingDB[fullKey];

    if (data) {
        // [ID 매칭] 수정된 DB 필드명(nickname, social, inner 등)에 맞게 연결
        const resName = document.getElementById('resultName') || document.getElementById('character-res');
        
        // 메인 제목에 별명과 괘 이름 출력
        if (resName) resName.innerHTML = `<small>${data.nickname}</small><br>${data.symbol} ${data.name}`;
        
        // 상세 섹션 업데이트
        if (document.getElementById('socialContent')) {
            // 태그와 MBTI 표시
            if(document.getElementById('resultTag')) document.getElementById('resultTag').innerText = `${data.tag} | ${data.mbti}`;
            
            // 기존 summary 대신 nickname이나 tag 활용 가능
            document.getElementById('resultSummary').innerText = data.tag; 
            document.getElementById('resultOneLine').innerText = data.nickname;
            
            // 핵심 본문 매핑
            document.getElementById('socialContent').innerText = data.social; // 사회적 가면
            document.getElementById('innerContent').innerText = data.inner;   // 내면의 본질
            document.getElementById('romanceContent').innerText = data.romance; // 연애 스타일 (추가된 경우)
            document.getElementById('adviceText').innerText = data.advice;   // 현생 가이드
        }

        return { 
            hexaName: data.name.split(' ')[0], 
            upper: upperName, 
            lower: lowerName,
            fullKey: fullKey
        };
    }
}

// 모든 괘 이름을 배열로 반환
function getAllHexagrams() {
    return Object.values(ichingDB).map(item => item.name.split(' ')[0]);
}

// [업그레이드] 이진수 비교를 통한 공명도 계산 (랜덤보다 신뢰도 높음)
function calculate64Score(myName, targetName) {
    if (myName === targetName) return 100;
    
    // ichingDB에서 두 괘의 binary key를 찾아 서로 다른 비트 개수(Hamming Distance) 계산
    const myKey = Object.keys(ichingDB).find(key => ichingDB[key].name.includes(myName));
    const targetKey = Object.keys(ichingDB).find(key => ichingDB[key].name.includes(targetName));
    
    if (!myKey || !targetKey) return 50;
    
    let diff = 0;
    for(let i=0; i<6; i++) { if(myKey[i] !== targetKey[i]) diff++; }
    
    // 차이가 적을수록(0에 가까울수록) 점수가 높음 (100 ~ 40점 사이)
    return 100 - (diff * 10); 
}
